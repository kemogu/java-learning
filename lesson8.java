public class lesson8 {
    public static void main(String[] args){
        
        int x = 5;
        double d = x;
        
        System.out.println(x); // 5
        System.out.println(d); // 5.0

        double y = 7.55d;
        int i = (int) y;

        System.out.println(y); // 7.55
        System.out.println(i); // 7
    } 
}
